
import jwt from "jsonwebtoken";
import User from "../models/User.Model.js";

const verifyToken = async (req, res, next) => {
    try {
        const token = req.cookies.jwt || req.headers.authorization?.split(" ")[1];
        
        console.log("VerifyToken - Cookies:", req.cookies);
        console.log("VerifyToken - Token:", token ? "Found" : "Missing");

        if (!token) {
            return res.status(401).json({ success: false, message: "Unauthorized - No Token Provided" });
        }

        const decoded = jwt.verify(token, process.env.JWT_SECRET);
        console.log("VerifyToken - Decoded:", decoded);

        if (!decoded) {
            return res.status(401).json({ success: false, message: "Unauthorized - Invalid Token" });
        }
        
        const user = await User.findById(decoded.userId).select("-password");
        console.log("VerifyToken - User Role:", user?.role);

        if (!user) {
            return res.status(401).json({ success: false, message: "Unauthorized - User Not Found" });
        }

        req.user = user;

        next();
    } catch (error) {
        console.log("Error in verifyToken middleware", error.message);
        if (error.name === "TokenExpiredError" || error.name === "JsonWebTokenError") {
            return res.status(401).json({ success: false, message: "Unauthorized - Invalid or Expired Token" });
        }
        res.status(500).json({ success: false, message: "Internal Server Error" });
    }
}

const isAdmin = async (req, res, next) => {
    try {
        console.log("IsAdmin Check - Role:", req.user?.role);
        if (req.user && req.user.role === "Admin") {
            next();
        } else {
            return res.status(403).json({ success: false, message: "Access denied - Admin only" });
        }
    } catch (error) {
       console.log("Error in isAdmin middleware", error.message);
       return res.status(500).json({ success: false, message: "Internal Server Error" });
    }
}

export { verifyToken, isAdmin };
